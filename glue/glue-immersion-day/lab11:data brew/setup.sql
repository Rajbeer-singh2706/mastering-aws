# Lab 11: Working with Glue Databrew

AWS Glue DataBrew is a visual data preparation tool that makes it easy for data analysts and data 
scientists to prepare data with an interactive, point-and-click visual interface without writing code. 
With Glue DataBrew, you can easily visualize, clean, and normalize terabytes, and even petabytes of data 
directly from your data lake, data warehouses, and databases, including Amazon S3, Amazon Redshift, 
Amazon Aurora, and Amazon RDS.

AWS Glue DataBrew is built for users who need to clean and normalize data for analytics and machine 
learning. Data analysts and data scientists are the primary users. For data analysts, examples of job 
functions are business intelligence analysts, operations analysts, market intelligence analysts, 
legal analysts, financial analysts, economists, quants, or accountants. For data scientists, examples 
of job functions are materials scientists, bioanalytical scientists, and scientific researchers.

